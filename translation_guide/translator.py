    # 용어 번역
    def translate_doc(self, text: str) -> Tuple[str, List[Dict]]:
        if not text:
            return "", []
        
        translated = text
        changes = []
        translated_found = set()

        sorted_terms = sorted(self.translate_dict.items(), key=lambda x: len(x[0]), reverse=True)

        # 용어 번역
        for original, simplified in sorted_terms:
            pattern = re.escape(original) + r"(?!\()"

            translated, count = re.subn(pattern, f"{original}({simplified})", translated)

            if count > 0:
                changes.append({
                    "original": original,
                    "simplified": simplified,
                    "type": "term",
                    "count": count
                })

        # 조사 및 부사 번역
        for pattern, replacement in self.pattern_dict.items():
            matches = re.findall(pattern, translated)

            if matches:
                translated = re.sub(pattern, replacement, translated)

                changes.append({
                    "original": pattern,
                    "simplified": replacement,
                    "type": "pattern",
                    "count": len(matches)
                })

        return translated, changes

    # 문장 구조 단순화
    def simplify_structure(self, text: str) -> str:
        text = re.sub(r'아니\s*할\s*수\s*없', '해야 함', text)
        text = re.sub(r'하시기\s*바랍니다', '하세요', text)
        text = re.sub(r'하여\s*주시기\s*바랍니다', '해주세요', text)
        text = re.sub(r'하여야\s*합니다', '해야 합니다', text)
        text = re.sub(r'함에\s*있어', '할 때', text)

        return text